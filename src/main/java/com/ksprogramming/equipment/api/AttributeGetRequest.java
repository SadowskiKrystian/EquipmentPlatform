package com.ksprogramming.equipment.api;

public class AttributeGetRequest {
    private String name;
    private String type;
    private String domain;

    public AttributeGetRequest() {
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getDomain() {
        return domain;
    }
}
