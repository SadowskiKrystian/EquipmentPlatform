package com.ksprogramming.equipment.api;

public class AttributePostRequest {
   private String name;
   private String type;
   private String domain;

    public AttributePostRequest() {
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getDomain() {
        return domain;
    }
}
